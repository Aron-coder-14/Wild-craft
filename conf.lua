function love.conf(t)
    t.window.title = "Wild Craft"  -- Set the window title
    t.window.width = 800          -- Default window width
    t.window.height = 600         -- Default window height
    t.window.resizable = true     -- Enable window resizing
    t.window.minwidth = 400       -- Minimum window width
    t.window.minheight = 300      -- Minimum window height
    t.window.fullscreen = false   -- Start in windowed mode
    t.window.vsync = 1            -- Enable vertical sync
end
